package kumon2014.kumondata;


public class WDownloadPrintSetData {
	
	public int 			mSoapStatus = 0;;
	public String 		mSoapError = "";
	public String		mLastSessionID = "";
	
	public DPrintSet	mDowunLoadPrintSet = null;
	
	public WDownloadPrintSetData()
	{
		mSoapStatus = 0;
		mSoapError = "";
		mLastSessionID = "";
		
		mDowunLoadPrintSet = new DPrintSet();
	}
	public void ClearDownLoadData()
	{
		mDowunLoadPrintSet.ClearAll();
	}
}
